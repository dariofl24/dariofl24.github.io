package com.carsdb.carsDBmongo.entity;

public class GalleryEntry
{
    private String full;

    private String half;

    private String thumb;

    public String getFull()
    {
        return full;
    }

    public void setFull(final String full)
    {
        this.full = full;
    }

    public String getHalf()
    {
        return half;
    }

    public void setHalf(final String half)
    {
        this.half = half;
    }

    public String getThumb()
    {
        return thumb;
    }

    public void setThumb(final String thumb)
    {
        this.thumb = thumb;
    }
}
